/* ─── Multiboot2 tag parsing ─────────────────────────────── */

/* Multiboot2 tag types */
#define MB2_TAG_END         0
#define MB2_TAG_FRAMEBUFFER 8

typedef struct {
    uint32_t type;
    uint32_t size;
} __attribute__((packed)) mb2_tag_t;

typedef struct {
    uint32_t type;
    uint32_t size;
    uint64_t framebuffer_addr;
    uint32_t framebuffer_pitch;
    uint32_t framebuffer_width;
    uint32_t framebuffer_height;
    uint8_t  framebuffer_bpp;
    uint8_t  framebuffer_type;
    uint16_t reserved;
} __attribute__((packed)) mb2_tag_framebuffer_t;

void fb_init_from_multiboot(uint32_t mb_info_addr) {
    memset(&fb_info, 0, sizeof(fb_info));
    fb_info.available = false;

    if (mb_info_addr == 0) {
        serial_puts("[FB] No multiboot info\n");
        return;
    }

    /* Multiboot2 info structure: first 8 bytes are total_size + reserved */
    uint32_t total_size = *(uint32_t*)mb_info_addr;
    (void)total_size;

    /* Tags start at offset 8 */
    uint8_t* tag_ptr = (uint8_t*)(mb_info_addr + 8);

    while (1) {
        mb2_tag_t* tag = (mb2_tag_t*)tag_ptr;

        if (tag->type == MB2_TAG_END) break;

        if (tag->type == MB2_TAG_FRAMEBUFFER) {
            mb2_tag_framebuffer_t* fb_tag = (mb2_tag_framebuffer_t*)tag;

            fb_info.address = (uint32_t*)(uint32_t)fb_tag->framebuffer_addr;
            fb_info.width   = fb_tag->framebuffer_width;
            fb_info.height  = fb_tag->framebuffer_height;
            fb_info.pitch   = fb_tag->framebuffer_pitch;
            fb_info.bpp     = fb_tag->framebuffer_bpp;
            fb_info.available = true;

            serial_puts("[FB] Framebuffer found: ");
            char buf[16];
            utoa(fb_info.width, buf, 10);
            serial_puts(buf);
            serial_puts("x");
            utoa(fb_info.height, buf, 10);
            serial_puts(buf);
            serial_puts("x");
            utoa(fb_info.bpp, buf, 10);
            serial_puts(buf);
            serial_puts(" @ 0x");
            utoa((uint32_t)fb_info.address, buf, 16);
            serial_puts(buf);
            serial_puts(" pitch=");
            utoa(fb_info.pitch, buf, 10);
            serial_puts(buf);
            serial_puts("\n");

            /* Identity-map framebuffer memory */
            uint32_t fb_size = fb_info.pitch * fb_info.height;
            uint32_t fb_start = (uint32_t)fb_info.address;
            for (uint32_t addr = fb_start; addr < fb_start + fb_size; addr += 4096) {
                vmm_map_page(addr, addr, PAGE_PRESENT | PAGE_WRITABLE);
            }

            /* Allocate backbuffer */
            backbuffer = (uint32_t*)kmalloc(fb_info.width * fb_info.height * 4);
            if (backbuffer) {
                serial_puts("[FB] Backbuffer allocated\n");
            }

            break;
        }

        /* Advance to next tag (aligned to 8 bytes) */
        uint32_t tag_size = (tag->size + 7) & ~7;
        tag_ptr += tag_size;
    }

    if (!fb_info.available) {
        serial_puts("[FB] No framebuffer in multiboot tags (text mode)\n");
    }
}

bool fb_is_available(void) {
    return fb_info.available;
}

framebuffer_info_t fb_get_info(void) {
    return fb_info;
}

/* ─── Drawing primitives ────────────────────────────────── */

static inline void _putpixel(uint32_t* buffer, int x, int y, color_t color) {
    if (x < 0 || y < 0 || (uint32_t)x >= fb_info.width || (uint32_t)y >= fb_info.height)
        return;
    buffer[y * (fb_info.pitch / 4) + x] = color;
}

void fb_putpixel(int x, int y, color_t color) {
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;
    _putpixel(target, x, y, color);
}

void fb_fill_rect(int x, int y, int w, int h, color_t color) {
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;
    uint32_t pitch4 = fb_info.pitch / 4;

    /* Clip */
    int x0 = x < 0 ? 0 : x;
    int y0 = y < 0 ? 0 : y;
    int x1 = (x + w) > (int)fb_info.width  ? (int)fb_info.width  : (x + w);
    int y1 = (y + h) > (int)fb_info.height ? (int)fb_info.height : (y + h);

    for (int py = y0; py < y1; py++) {
        uint32_t* row = &target[py * pitch4 + x0];
        for (int px = x0; px < x1; px++) {
            *row++ = color;
        }
    }
}

void fb_draw_rect(int x, int y, int w, int h, color_t color, int thickness) {
    fb_fill_rect(x, y, w, thickness, color);             /* top */
    fb_fill_rect(x, y + h - thickness, w, thickness, color); /* bottom */
    fb_fill_rect(x, y, thickness, h, color);              /* left */
    fb_fill_rect(x + w - thickness, y, thickness, h, color); /* right */
}

void fb_fill_rounded_rect(int x, int y, int w, int h, int radius, color_t color) {
    /* Main body */
    fb_fill_rect(x + radius, y, w - 2*radius, h, color);
    fb_fill_rect(x, y + radius, radius, h - 2*radius, color);
    fb_fill_rect(x + w - radius, y + radius, radius, h - 2*radius, color);

    /* Corners — filled circles */
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;
    int cx, cy;
    int r2 = radius * radius;

    /* Top-left */
    cx = x + radius; cy = y + radius;
    for (int dy = -radius; dy < 0; dy++)
        for (int dx = -radius; dx < 0; dx++)
            if (dx*dx + dy*dy <= r2)
                _putpixel(target, cx+dx, cy+dy, color);

    /* Top-right */
    cx = x + w - radius - 1; cy = y + radius;
    for (int dy = -radius; dy < 0; dy++)
        for (int dx = 0; dx <= radius; dx++)
            if (dx*dx + dy*dy <= r2)
                _putpixel(target, cx+dx, cy+dy, color);

    /* Bottom-left */
    cx = x + radius; cy = y + h - radius - 1;
    for (int dy = 0; dy <= radius; dy++)
        for (int dx = -radius; dx < 0; dx++)
            if (dx*dx + dy*dy <= r2)
                _putpixel(target, cx+dx, cy+dy, color);

    /* Bottom-right */
    cx = x + w - radius - 1; cy = y + h - radius - 1;
    for (int dy = 0; dy <= radius; dy++)
        for (int dx = 0; dx <= radius; dx++)
            if (dx*dx + dy*dy <= r2)
                _putpixel(target, cx+dx, cy+dy, color);
}

void fb_draw_rounded_rect(int x, int y, int w, int h, int radius, color_t color) {
    /* Top/bottom edges */
    fb_fill_rect(x + radius, y, w - 2*radius, 1, color);
    fb_fill_rect(x + radius, y + h - 1, w - 2*radius, 1, color);
    /* Left/right edges */
    fb_fill_rect(x, y + radius, 1, h - 2*radius, color);
    fb_fill_rect(x + w - 1, y + radius, 1, h - 2*radius, color);

    /* Corner arcs (Bresenham) */
    int f = 1 - radius, ddF_x = 0, ddF_y = -2 * radius;
    int px = 0, py = radius;
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;

    while (px < py) {
        if (f >= 0) { py--; ddF_y += 2; f += ddF_y; }
        px++; ddF_x += 2; f += ddF_x + 1;

        _putpixel(target, x+radius-px, y+radius-py, color);
        _putpixel(target, x+radius-py, y+radius-px, color);
        _putpixel(target, x+w-1-radius+px, y+radius-py, color);
        _putpixel(target, x+w-1-radius+py, y+radius-px, color);
        _putpixel(target, x+radius-px, y+h-1-radius+py, color);
        _putpixel(target, x+radius-py, y+h-1-radius+px, color);
        _putpixel(target, x+w-1-radius+px, y+h-1-radius+py, color);
        _putpixel(target, x+w-1-radius+py, y+h-1-radius+px, color);
    }
}

void fb_draw_line(int x0, int y0, int x1, int y1, color_t color) {
    int dx = x1 - x0, dy = y1 - y0;
    int sx = dx > 0 ? 1 : -1, sy = dy > 0 ? 1 : -1;
    if (dx < 0) dx = -dx;
    if (dy < 0) dy = -dy;

    int err = (dx > dy ? dx : -dy) / 2;
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;

    while (1) {
        _putpixel(target, x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        int e2 = err;
        if (e2 > -dx) { err -= dy; x0 += sx; }
        if (e2 < dy)  { err += dx; y0 += sy; }
    }
}

void fb_fill_circle(int cx, int cy, int r, color_t color) {
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;
    int r2 = r * r;
    for (int dy = -r; dy <= r; dy++)
        for (int dx = -r; dx <= r; dx++)
            if (dx*dx + dy*dy <= r2)
                _putpixel(target, cx+dx, cy+dy, color);
}

void fb_draw_circle(int cx, int cy, int r, color_t color) {
    int x = 0, y = r, d = 3 - 2 * r;
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;

    while (y >= x) {
        _putpixel(target, cx+x, cy+y, color);
        _putpixel(target, cx-x, cy+y, color);
        _putpixel(target, cx+x, cy-y, color);
        _putpixel(target, cx-x, cy-y, color);
        _putpixel(target, cx+y, cy+x, color);
        _putpixel(target, cx-y, cy+x, color);
        _putpixel(target, cx+y, cy-x, color);
        _putpixel(target, cx-y, cy-x, color);
        if (d < 0) d += 4 * x + 6;
        else { d += 4 * (x - y) + 10; y--; }
        x++;
    }
}

void fb_clear(color_t color) {
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;
    uint32_t total = fb_info.width * fb_info.height;
    for (uint32_t i = 0; i < total; i++) {
        target[i] = color;
    }
}

/* ─── Alpha blending ────────────────────────────────────── */

color_t fb_blend(color_t bg, color_t fg) {
    uint8_t alpha = (fg >> 24) & 0xFF;
    if (alpha == 0xFF) return fg;
    if (alpha == 0x00) return bg;

    uint8_t inv = 255 - alpha;
    uint8_t r = ((((fg >> 16) & 0xFF) * alpha) + (((bg >> 16) & 0xFF) * inv)) / 255;
    uint8_t g = ((((fg >> 8)  & 0xFF) * alpha) + (((bg >> 8)  & 0xFF) * inv)) / 255;
    uint8_t b = ((((fg)       & 0xFF) * alpha) + (((bg)       & 0xFF) * inv)) / 255;

    return 0xFF000000 | (r << 16) | (g << 8) | b;
}

void fb_fill_rect_alpha(int x, int y, int w, int h, color_t color) {
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;
    uint32_t pitch4 = fb_info.pitch / 4;

    int x0 = x < 0 ? 0 : x;
    int y0 = y < 0 ? 0 : y;
    int x1 = (x + w) > (int)fb_info.width  ? (int)fb_info.width  : (x + w);
    int y1 = (y + h) > (int)fb_info.height ? (int)fb_info.height : (y + h);

    for (int py = y0; py < y1; py++) {
        for (int px = x0; px < x1; px++) {
            uint32_t idx = py * pitch4 + px;
            target[idx] = fb_blend(target[idx], color);
        }
    }
}

/* ─── Text rendering ────────────────────────────────────── */

void fb_putchar(int x, int y, char c, color_t fg, color_t bg) {
    if (c < 32 || c > 126) c = '?';
    const uint8_t* glyph = font_8x16[c - 32];

    uint32_t* target = backbuffer ? backbuffer : fb_info.address;
    uint32_t pitch4 = fb_info.pitch / 4;

    for (int row = 0; row < FONT_HEIGHT; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < FONT_WIDTH; col++) {
            int px = x + col;
            int py = y + row;
            if (px >= 0 && py >= 0 && (uint32_t)px < fb_info.width && (uint32_t)py < fb_info.height) {
                target[py * pitch4 + px] = (bits & (0x80 >> col)) ? fg : bg;
            }
        }
    }
}

void fb_puts(int x, int y, const char* str, color_t fg, color_t bg) {
    while (*str) {
        if (*str == '\n') { y += FONT_HEIGHT; x = 0; }
        else {
            fb_putchar(x, y, *str, fg, bg);
            x += FONT_WIDTH;
        }
        str++;
    }
}

void fb_puts_transparent(int x, int y, const char* str, color_t fg) {
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;
    uint32_t pitch4 = fb_info.pitch / 4;

    while (*str) {
        if (*str == '\n') { y += FONT_HEIGHT; x = 0; str++; continue; }
        char c = (*str < 32 || *str > 126) ? '?' : *str;
        const uint8_t* glyph = font_8x16[c - 32];
        for (int row = 0; row < FONT_HEIGHT; row++) {
            uint8_t bits = glyph[row];
            for (int col = 0; col < FONT_WIDTH; col++) {
                if (bits & (0x80 >> col)) {
                    int px = x + col, py = y + row;
                    if (px >= 0 && py >= 0 && (uint32_t)px < fb_info.width && (uint32_t)py < fb_info.height)
                        target[py * pitch4 + px] = fg;
                }
            }
        }
        x += FONT_WIDTH;
        str++;
    }
}

int fb_text_width(const char* str) {
    int w = 0;
    while (*str) { if (*str != '\n') w += FONT_WIDTH; str++; }
    return w;
}

/* ─── Double-buffering ──────────────────────────────────── */

void fb_swap(void) {
    if (!backbuffer || !fb_info.address) return;
    memcpy(fb_info.address, backbuffer, fb_info.width * fb_info.height * 4);
}

void fb_copy_region(int x, int y, int w, int h) {
    if (!backbuffer || !fb_info.address) return;
    uint32_t pitch4 = fb_info.pitch / 4;
    for (int py = y; py < y + h && (uint32_t)py < fb_info.height; py++) {
        memcpy(&fb_info.address[py * pitch4 + x],
               &backbuffer[py * pitch4 + x],
               w * 4);
    }
}

/* ─── Mouse cursor ──────────────────────────────────────── */

/* Simple 12x19 arrow cursor */
static const uint8_t cursor_data[19] = {
    0b10000000, 0b11000000, 0b11100000, 0b11110000,
    0b11111000, 0b11111100, 0b11111110, 0b11111111,
    0b11111100, 0b11111100, 0b11001100, 0b10000110,
    0b00000110, 0b00000011, 0b00000011, 0b00000001,
    0b00000001, 0b00000000, 0b00000000,
};

void fb_draw_cursor(int x, int y) {
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;
    for (int row = 0; row < 19; row++) {
        uint8_t bits = cursor_data[row];
        for (int col = 0; col < 8; col++) {
            if (bits & (0x80 >> col)) {
                _putpixel(target, x+col, y+row, COLOR_WHITE);
            }
        }
    }
}

void fb_scroll_up(int pixels, color_t fill_color) {
    uint32_t* target = backbuffer ? backbuffer : fb_info.address;
    uint32_t pitch4 = fb_info.pitch / 4;

    memmove(target, &target[pixels * pitch4],
            (fb_info.height - pixels) * fb_info.pitch);

    for (uint32_t y = fb_info.height - pixels; y < fb_info.height; y++) {
        for (uint32_t x = 0; x < fb_info.width; x++) {
            target[y * pitch4 + x] = fill_color;
        }
    }
}
